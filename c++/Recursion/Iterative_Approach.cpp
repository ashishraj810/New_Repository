             //A Happy Birthday countdown//

#include<iostream>
using namespace std;

int main()
{
    int n=7;
    for(int i=n;i>0;i--)
    {
        if(i!=1)
       cout<<i<<" days left for birthday.\n";

       else if(i==1)
       {
        cout<<"Only 1 day left for birthday.\n";
       }
       
    }

    cout<<"\nIT'S YOUR BIRTHDAY TODAY...A VERY HAPPY BIRTHDAY";

   
    
}